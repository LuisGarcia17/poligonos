class poligono():
    lado=5
    altura=7
    base=10
    ladot=11

class cuadrado():
    print("El Area de el Cuadrado es",poligono.lado*poligono.lado)
    print("El perimetro de el Cuadrado es",poligono.lado+poligono.lado+poligono.lado+poligono.lado)
    
class triangulo():
    print("El Area de el triangulo es",poligono.base*poligono.altura/2)
    print("El perimetro de el Triangulo es",poligono.ladot+poligono.ladot+poligono.ladot)